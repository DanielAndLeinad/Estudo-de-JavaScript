/*
OPERADORES LOGICOS
&& -> AND -> E
|| -> OR -> OU
! -> NOT -> NAO

*/

// EXEMPLOS LOGICOS



console.log(true && true); // os valores precisam ser verdadeiro para retornar true
console.log(true || false); // um é verdadeiro, então retorna tru
console.log(!true); // inverte uma expressao. Se for true = false & se for false = true
console.log(!!false); // invertendo o valor duas vezes. Uma vez para true e depois para false (!!) 
